SELECT  
	ROW_NUMBER() over(order by [EMPLOYEENO]) as ID,
	[EMPLOYEENO] 
,DR.EMPLOYEE
,DR.DEPARTMENT
,DR.UNIT
,(case when [ATTENDANCE]='0.0' then '' else [ATTENDANCE] end )  AS [ATTENDANCE]
,[OVERTIME] 
,[HOLIDAYH] 
,[HOLIDAYC] 
,[HOLIDAYE] 
,[HOLIDAYD] 
,[HOLIDAYF] 
,[HOLIDAYB] 
,[HOLIDAYA] 
,[HOLIDAYI] 
,[HOLIDAYG] 
,[HOLIDAYO] 
,(case when[NOTWORK] ='0.0' then '' else [NOTWORK]  end )  AS [NOTWORK]
,(case when [BELATE]='0' then '' else [BELATE] end ) AS [BELATE]
,(case when [EARLY]='0' then '' else [EARLY] end ) AS [EARLY]
,[STOPWORK] 
,[MEALS] 
,[NOTE] 
  FROM [hr].[dbo].[VN_DAY_REPORT] DR
  JOIN  VN_UNIT VU ON DR.UNIT=VU.UNIT
JOIN VN_DEPARTMENT VD ON VD.DEPARTMENT=DR.DEPARTMENT
WHERE DAY='<DAY/>'
AND <DEPT/>
AND <UNIT/>
order by DR.DEPARTMENT DESC, DR.UNIT DESC